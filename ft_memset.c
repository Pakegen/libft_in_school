#include"libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*ptr;

	ptr = (unsigned char *) s;
	while (0 < n)
	{
		*ptr = c;
		n--;
		ptr++;
	}
	return (ptr);
}
